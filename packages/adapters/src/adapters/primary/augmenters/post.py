class PostAugmenter(Augmentation[PostEntity]):
    def __init__(
        self,
        collection: AsyncCollection,
        collection_name: CollectionName,
    ) -> None:
        super().__init__(collection, collection_name)
        self.accounts = []

    def process_line(self, line: str) -> List[str]:
        # Fun robot that engages kids with games and activities.,http://dummyimage.com/228x148.png/cc0000/ffffff
        parts = line.strip().split(",http")
        if len(parts) != 2:
            raise ValueError(f"Invalid line format: {line}")

        return [parts[0], "http" + parts[1]]

    def provide_accounts(self, accounts: List[str]) -> None:
        self.accounts = accounts

    async def insert_one(self, attrs=None) -> Optional[PostEntity]:
        if attrs is None:
            attrs = []

        [title, image_url] = attrs
        if not self.accounts:
            return None

        # random data for generating post
        # random account id in accounts
        # random 2 - 3 paragraphs from content
        # random 3 - 5 tags from tags

        account_id = random.choice(self.accounts)
        captions = random.sample(content, k=random.randint(2, 3))
        selected_tags = random.sample(tags, k=random.randint(3, 5))

        entity = PostEntity.create(
            account_id=account_id,
            image_url=image_url,
            title=title,
            captions=captions,
            tags=selected_tags,
        )

        return await self.create(entity)
